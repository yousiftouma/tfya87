package com.mygdx.game;

/**
 * Enum representing each side of the CollisionEntity hitBox, used for proper collisionhandling
 */
public enum Side {
    /**
     * Top of object
     */
    TOP,
    /**
     * Bottom of object
     */
    BOTTOM,
    /**
     * Left of object
     */
    LEFT,
    /**
     * Right of object
     */
    RIGHT
}
